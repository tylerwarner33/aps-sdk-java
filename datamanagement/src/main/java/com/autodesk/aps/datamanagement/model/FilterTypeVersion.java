package com.autodesk.aps.datamanagement.model;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonValue;

/**
 * Filter by the &#x60;&#x60;type&#x60;&#x60; of the &#x60;&#x60;ref&#x60;&#x60;
 * target. Supported values include &#x60;&#x60;folders&#x60;&#x60;,
 * &#x60;&#x60;items&#x60;&#x60;, and &#x60;&#x60;versions&#x60;&#x60;.
 */
public enum FilterTypeVersion {

  FOLDERS("folders"),

  ITEMS("items"),

  VERSIONS("versions");

  private String value;

  FilterTypeVersion(String value) {
    this.value = value;
  }

  @JsonValue
  public String getValue() {
    return value;
  }

  @Override
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static FilterTypeVersion fromValue(String value) {
    for (FilterTypeVersion b : FilterTypeVersion.values()) {
      if (b.value.equals(value)) {
        return b;
      }
    }
    throw new IllegalArgumentException("Unexpected value '" + value + "'");
  }
}
