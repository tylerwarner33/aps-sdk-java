package com.autodesk.aps.modelderivative.model;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonValue;

/**
 * Specifies the format of the file to create, when the specified output is STL.
 * Possible values are: - &#x60;&#x60;ascii&#x60;&#x60; - Create derivative as
 * an ASCII STL file. - &#x60;&#x60;binary&#x60;&#x60; - (Default) Create
 * derivative as a binary STL file.
 */
public enum Format {

  BINARY("binary"),

  ASCII("ascii");

  private String value;

  Format(String value) {
    this.value = value;
  }

  @JsonValue
  public String getValue() {
    return value;
  }

  @Override
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static Format fromValue(String value) {
    for (Format b : Format.values()) {
      if (b.value.equals(value)) {
        return b;
      }
    }
    throw new IllegalArgumentException("Unexpected value '" + value + "'");
  }
}
