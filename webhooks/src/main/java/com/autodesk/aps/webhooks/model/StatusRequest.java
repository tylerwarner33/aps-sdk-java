package com.autodesk.aps.webhooks.model;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonValue;

/**
 * Sets the current state of the webhook. Possible values: -
 * &#x60;&#x60;active&#x60;&#x60; - Activates webhook. -
 * &#x60;&#x60;inactive&#x60;&#x60; - Deactivates webhook.
 */
public enum StatusRequest {

  ACTIVE("active"),

  INACTIVE("inactive");

  private String value;

  StatusRequest(String value) {
    this.value = value;
  }

  @JsonValue
  public String getValue() {
    return value;
  }

  @Override
  public String toString() {
    return String.valueOf(value);
  }

  @JsonCreator
  public static StatusRequest fromValue(String value) {
    for (StatusRequest b : StatusRequest.values()) {
      if (b.value.equals(value)) {
        return b;
      }
    }
    throw new IllegalArgumentException("Unexpected value '" + value + "'");
  }
}
